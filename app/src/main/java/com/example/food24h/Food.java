package com.example.food24h;

public class Food {
    public String name_food;
    public String image;
    public String text;
    public String sl;
    public String price;

    public Food() {
    }

    public Food(String name_food, String image, String text, String sl, String price) {
        this.name_food = name_food;
        this.image = image;
        this.text = text;
        this.sl = sl;
        this.price = price;
    }
}
