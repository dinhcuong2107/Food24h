package com.example.food24h;

public class Drink {
    public String name_drink;
    public String image;
    public String text;
    public String sl;
    public String price;

    public Drink() {
    }

    public Drink(String name_drink, String image, String text, String sl, String price) {
        this.name_drink = name_drink;
        this.image = image;
        this.text = text;
        this.sl = sl;
        this.price = price;
    }
}
